K = 3; % （K-1）次Bースプライン
q = [1; 2; 4; 6; 7; 8; 9]; % 節点のを設定
alpha = [1; 2; -0.5; 1]; % 係り数を設定

x = linspace(0, 11, 100)'; % ｘ座標を設定
y = fun_spline(x, K, q, alpha); % 関数値を求める

figure(1); % 描画ウィンドウ１を開く
